package iniciante;

public class P2752 {

	public static void main(String[] args) {
		String var = "AMO FAZER EXERCICIO NO URI";
		
		System.out.printf("<%s>\n", var);
		System.out.printf("<%30s>\n", var);
		System.out.printf("<%.20s>\n", var);
		System.out.printf("<%-20s>\n", var);
		System.out.printf("<%-30s>\n", var);
		System.out.printf("<%.30s>\n", var);
		System.out.printf("<%30.20s>\n", var);
		System.out.printf("<%-30.20s>\n", var);
	}

}
